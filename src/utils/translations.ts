export const useTranslation = (lang: Language) => {
  const t = (key: keyof typeof translations['en'], params?: Record<string, string | number>) => {
    let text = translations[lang][key] || translations['en'][key] || String(key);
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        text = text.replace(`{${k}}`, String(v));
      });
    }
    return text;
  };

  return { t };
};

/**
 * Formats user phone number to be like 0926****20
 */
export function formatUserPhoneId(phone: string): string {
  if (!phone) return '';
  const digits = phone.replace(/\D/g, '');
  
  if (digits.length >= 10) {
    let nationalDigits = digits;
    if (digits.startsWith('251') && digits.length === 12) {
      nationalDigits = '0' + digits.slice(3);
    }
    
    if (nationalDigits.length === 10) {
      return `${nationalDigits.slice(0, 4)}****${nationalDigits.slice(-2)}`;
    }
    
    return `${nationalDigits.slice(0, 4)}****${nationalDigits.slice(-2)}`;
  }
  
  if (phone.length <= 4) return phone;
  return `${phone.slice(0, 2)}****${phone.slice(-2)}`;
}

/**
 * Replaces any phone numbers found inside a string with their masked format
 */
export function formatPhoneNumbersInText(text: string): string {
  if (!text) return '';
  return text.replace(/\b(251)?(0?[79]\d{8})\b/g, (match) => {
    return formatUserPhoneId(match);
  });
}
